function calculaMedia(num1, num2, num3) {
    const soma = num1 + num2 + num3;
    const media = soma / 3;
    return media;
  }
  

  const numero1 = 10;
  const numero2 = 20;
  const numero3 = 30;
  
  const mediaResultado = calculaMedia(numero1, numero2, numero3);
  
  console.log(`A média é : ${mediaResultado}`);
  